char to_hex(uint8_t v)
{
  if (v < 0xA)
  {
    return '0' + v;
  }
  return 'A' + (v - 0xA);
}

void HAL_CAN_RxFifo0MsgPendingCallback(CAN_HandleTypeDef *hcan) {
  HAL_GPIO_WritePin(LD6_GPIO_Port, LD6_Pin, GPIO_PIN_SET);

  CAN_RxHeaderTypeDef header;
  uint8_t data[8];
  if (HAL_CAN_GetRxMessage(&hcan1, CAN_RX_FIFO0, &header, data) == HAL_OK) {
    CAN_TxHeaderTypeDef txHeader;
    txHeader.StdId = header.StdId + 0x40;
    txHeader.ExtId = header.ExtId;
    txHeader.IDE = header.IDE;
    txHeader.RTR = CAN_RTR_DATA;
    txHeader.DLC = header.DLC;
    txHeader.TransmitGlobalTime = DISABLE;

    uint32_t mailbox;
    if (HAL_CAN_AddTxMessage(&hcan1, &txHeader, data, &mailbox) != HAL_OK)
      HAL_GPIO_WritePin(LD3_GPIO_Port, LD3_Pin, GPIO_PIN_SET);

    // Convert to hex

    char converted[64];
    int count = sprintf(converted, "[%lX] ", header.StdId);
    char * pos = converted + count;
    for (int i = 0; i < header.DLC; ++i)
    {
      uint8_t v = data[i];
      *pos++ = to_hex(v & 0xFU);
      *pos++ = to_hex(v >> 4U);
      if (i != header.DLC)
        *pos++ = ' ';
    }
    *pos++ = '\r';
    *pos++ = '\n';

    CDC_Transmit_FS((uint8_t*)converted, pos - converted);
  }
}

